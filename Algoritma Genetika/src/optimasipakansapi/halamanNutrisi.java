    private void buatTable() {
        model = new DefaultTableModel();
        model.addColumn("Nama Pakan");
        model.addColumn("Harga(/Kg)");
        model.addColumn("BK(%)");
        model.addColumn("PK(%)");
        model.addColumn("TDN(%)");
        model.addColumn("Ca(%)");
        model.addColumn("P(%)");
        tblNutrisi.setModel(model);
    }

    private void tampilTable() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        listNutrisi.clear();
        listNutrisi = ps.tampil(Pakan1, Pakan2, Pakan3, Pakan4);
        for (x = 0; x < listNutrisi.size(); x++) {
            Object[] data = new Object[7];
            data[0] = listNutrisi.get(x).getBahan_pakan();
            data[1] = listNutrisi.get(x).getHarga();
            data[2] = listNutrisi.get(x).getBK();
            data[3] = listNutrisi.get(x).getPK();
            data[4] = listNutrisi.get(x).getTDN();
            data[5] = listNutrisi.get(x).getCa();
            data[6] = listNutrisi.get(x).getP();
            model.addRow(data);
        }
    }

    private void clearTable() {
        DefaultTableModel model1 = (DefaultTableModel) tblNutrisi.getModel();
        while (model1.getRowCount() > 0) {
            for (int i = 0; i < model1.getRowCount(); i++) {
                model1.removeRow(i);
            }
        }
    }

