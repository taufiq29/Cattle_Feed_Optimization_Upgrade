    private void btnhitungnutrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhitungnutrisiActionPerformed
        // TODO add your handling code here:
        pbb = (Double) combopbb.getSelectedItem();
        ps.setPbb(pbb);
        ps.setBeratBadan(BB);

        listNutrisi.clear();
        listNutrisi = ps.tampilKebutuhan(BB, pbb, Jenis_Sapi);
        txtBK.setText(String.valueOf(listNutrisi.get(0).getBK()));
        txtPK.setText(String.valueOf(listNutrisi.get(0).getPK()));
        txtTDN.setText(String.valueOf(listNutrisi.get(0).getTDN()));
        txtCa.setText(String.valueOf(listNutrisi.get(0).getCa()));
        txtP.setText(String.valueOf(listNutrisi.get(0).getP()));
        double pakan_min = Double.valueOf(df.format(BB * (listNutrisi.get(0).getKemampuan_konsumsi()) / 100));
        if (pbb != 0) {
            txtPakanMin.setText(String.valueOf(pakan_min));
        } else {
            txtPakanMin.setText(String.valueOf(listNutrisi.get(0).getBK()));
        }
        txthijauan.setText(String.valueOf(Double.valueOf(df.format(pakan_min * 0.6))));
        txtkonsentrat.setText(String.valueOf(Double.valueOf(df.format(pakan_min * 0.4))));

        buatTable();
        tampilTable();
    }//GEN-LAST:event_btnhitungnutrisiActionPerformed

